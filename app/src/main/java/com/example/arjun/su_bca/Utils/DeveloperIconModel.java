package com.example.arjun.su_bca.Utils;

public class DeveloperIconModel {

    private Integer image;
    private String link;

    public DeveloperIconModel(Integer image, String link) {
        this.image = image;
        this.link = link;
    }

    public Integer getImage() {
        return image;
    }

    public void setImage(Integer image) {
        this.image = image;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

}
